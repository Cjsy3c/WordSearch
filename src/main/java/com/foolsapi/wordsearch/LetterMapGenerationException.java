package com.foolsapi.wordsearch;

public class LetterMapGenerationException extends RuntimeException {

	private static final long serialVersionUID = -6027489944631999507L;
	
	public LetterMapGenerationException() {
		super();
	}

	public LetterMapGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public LetterMapGenerationException(String message) {
		super(message);
	}

	public LetterMapGenerationException(Throwable cause) {
		super(cause);
	}

	
}
